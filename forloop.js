// if you have to print 'Hello world' five times.


/// Example of for loop

for (let i = 0; i < 5; i++) {
    console.log('Hello world');
}
//////////////////////////////////////



// Print even number
for (let i = 1; i <= 5; i++) {
    if(i % 2 !== 0) console.log(i);
}
////////////////////////////////////////
// Print even number in reverse order
// i-- reverse the order
for (let i = 5; i >= 1; i--) {
    if(i % 2 !== 0) console.log(i);
}